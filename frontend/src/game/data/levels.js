const coinLine = (startX, y, count, gap = 48, kind = "coin") =>
  Array.from({ length: count }, (_, index) => ({
    id: `${kind}-${startX}-${y}-${index}`,
    kind,
    x: startX + index * gap,
    y,
  }));

const floatingPlatform = (id, x, y, w, h, moving = null) => ({
  id,
  x,
  y,
  w,
  h,
  moving,
});

const enemy = (id, type, x, y, extras = {}) => ({
  id,
  type,
  x,
  y,
  ...extras,
});

const box = (id, x, y) => ({ id, x, y, w: 42, h: 42 });

const trap = (id, x, y, w, h, type = "spikes") => ({ id, x, y, w, h, type });

